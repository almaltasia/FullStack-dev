# Data Types
# A. Integers
my_integer = 10
print("Integer:", my_integer, "| Type:", type(my_integer))

# B. Floats
my_float = 10.5
print("Float:", my_float, "| Type:", type(my_float))

# C. Complex
my_complex = 2 + 3j
print("Complex:", my_complex, "| Type:", type(my_complex))

# D. Strings
my_string = 'Hello, Alma!'
print("String:", my_string, "| Type:", type(my_string))

# E. Boolean
my_boolean = False
print("Boolean:", my_boolean, "| Type:", type(my_boolean))

# F. Set
my_set = {1, 2, 3, 4, 5}
print("Set:", my_set, "| Type:", type(my_set))

# G. Dictionary
my_dict = {'name': 'Alma', 'age': 25}
print("Dictionary:", my_dict, "| Type:", type(my_dict))

# H. Tuple
my_tuple = (9,3,2,6,0)
print("Tuple:", my_tuple, "| Type:", type(my_tuple))

# I. Range
my_range = range(1, 10)
print("Range:", my_range, "| Type:", type(my_range))

# J. List
my_list = [14, "Hallo", 3.5, True]
print("List:", my_list, "| Type:", type(my_list))

# K. None
my_none = None
print("None:", my_none, "| Type:", type(my_none))